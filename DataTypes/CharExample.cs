using System;

namespace DataTypes
{
    public class CharExample
    {
        public static void Run()
        {
            // Display the title
            Console.WriteLine("----- CHAR DATA TYPE -----");

            // 'char' stores only one character.
            // Use single quotes.
            char grade = 'A';

            // Display the value.
            Console.WriteLine("Grade : " + grade);

            Console.WriteLine();
        }
    }
}