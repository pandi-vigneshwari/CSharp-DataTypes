using System;

namespace DataTypes
{
    public class FloatExample
    {
        public static void Run()
        {
            // Display the title
            Console.WriteLine("----- FLOAT DATA TYPE -----");

            // 'float' is used to store decimal numbers.
            // Add 'f' at the end of the value.
            float percentage = 89.5f;

            // Display the value.
            Console.WriteLine("Percentage : " + percentage);

            Console.WriteLine();
        }
    }
}