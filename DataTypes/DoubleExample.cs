using System;

namespace DataTypes
{
    public class DoubleExample
    {
        public static void Run()
        {
            // Display the title
            Console.WriteLine("----- DOUBLE DATA TYPE -----");

            // 'double' stores decimal numbers with more precision than float.
            double salary = 25000.75;

            // Display the value.
            Console.WriteLine("Salary : " + salary);

            Console.WriteLine();
        }
    }
}