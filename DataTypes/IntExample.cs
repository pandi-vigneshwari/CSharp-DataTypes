using System;

namespace DataTypes
{
    public class IntExample
    {
        public static void Run()
        {
            // Display the title of this example
            Console.WriteLine("----- INT DATA TYPE -----");

            // 'int' is used to store whole numbers.
            // It can store positive and negative values.
            int age = 22;

            // Display the value stored in the variable.
            Console.WriteLine("Age : " + age);

            // Leave one blank line after the output.
            Console.WriteLine();
        }
    }
}